#include "Calculations.h"


  //function to find sum of two numbers
    double Calculations::add(double a, double v)
    {
        return a + v;
    }


    //function to find difference
    double Calculations::subs(double a, double b)
    {
        return a - b;
    }


    //function to find product of two numbers
    double Calculations::prod(double a, double b)
    {
        return a * b;
    }
